#define TT167 twoSum2
TEST(leetcode,t167) {
    vector<int> numbers;
    vector<int> answer;
    numbers = {2, 7, 11, 15};
    answer = {1, 2};
    EXPECT_EQ(Leetcode().TT167(numbers, 9), answer) << "默认";
    numbers = {-1, 0};
    answer = {1, 2};
    EXPECT_EQ(Leetcode().TT167(numbers, -1), answer) << "默认";
}
